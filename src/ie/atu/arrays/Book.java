package ie.atu.arrays;

public class Book {
    private String title;
    private String author;
    
    // Constructor
    Book(String title, String author) {
        this.title = title;
        this.author = author;
    }
    
    // Getter methods
    String getTitle() {
        return title;
    }
    
    String getAuthor() {
        return author;
    }
}
